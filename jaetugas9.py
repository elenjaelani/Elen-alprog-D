# suhu dalam celcius = 25
# suhu dalam fahrenheit = 77.0
# suhu dalam kelvin = 298.15
# suhu dalam reamur = 20.0

# suhu dalam celcius
celcius = 25

# rumus koversi suhu
farenheit = (celcius * 9/5) + 32
kelvin = celcius + 273.15
reamur = celcius * 4/5

# cetak nilai suhu
print("suhu dalam fahrenheit:", farenheit )
print("suhu dalam kelvin:", kelvin)
print("suhu dalam reamur:", reamur)